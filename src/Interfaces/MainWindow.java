    /**
     * This method is used when Simulate button is pressed in order to run the scheduling process automatically without going through the step by step process
     */
    private void btnSimulateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimulateActionPerformed
        final ScheduledExecutorService service = Executors.newSingleThreadScheduledExecutor();
        service.scheduleWithFixedDelay(new Runnable()
        {
            @Override
            public void run()
            {
                LIMIT++;
                scheduler = new Schedular();
                list = scheduler.run(q1,q2,q3,q4,q5,txtA1,txtB1,txtC1,txtD1,txtE1,txtA2,txtB2,txtC2,txtD2,txtE2);
                if(LIMIT<=limit) Simulate(list,LIMIT, limit);
                if(LIMIT<=limit) btnTime.setText(String.valueOf(LIMIT));
            }
        }, 0, 2, TimeUnit.SECONDS);
    }//GEN-LAST:event_btnSimulateActionPerformed

